CREATE DATABASE AULASQL;
USE AULASQL;

CREATE TABLE CLIENTES(
	ID INT PRIMARY KEY AUTO_INCREMENT,
	NOME VARCHAR(100),
    DATA_DE_NASCIMENTO DATE,
    UF VARCHAR(2),
    EMAIL VARCHAR(50));
      
CREATE TABLE PEDIDOS(
	ID INT PRIMARY KEY AUTO_INCREMENT,
    ID_CLIENTE INT,
    COD_ORIGEM INT,
    NUMERO_PEDIDO INT,
	VALOR_TOTAL_PEDIDO FLOAT,
    DATA_PEDIDO DATE,
    FOREIGN KEY(ID_CLIENTE) REFERENCES CLIENTES(ID));
    
